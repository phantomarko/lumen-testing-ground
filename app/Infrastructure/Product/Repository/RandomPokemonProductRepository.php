    /**
     * @inheritDoc
     */
    public function findAll(): array
    {
        $pokemons = [];
        $ram = new RamseyUuidGenerator();
        foreach ($this->getPokemonList() as $pokemon) {
            $pokemons[] = new Product(
                $ram->generate(),
                new ProductName($pokemon),
                new Price(rand(1,PHP_INT_MAX), Currency::euro())
            );
        }
        return $pokemons;
    }
